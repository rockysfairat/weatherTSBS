import type { NextPage } from "next";
import { useState, useEffect } from "react";
// NextJS components:
import Head from "next/head";
import Image from "next/image";
// Custom components:
import { Weather } from "./components/weather";
// CSS:
import styles from "../styles/Home.module.css";

const Home: NextPage = (weatherData) => {
  // User Location:
  const [lat, setLat] = useState<number | null>(null);
  const [long, setLong] = useState<number | null>(null);
  useEffect(() => {
    const geolocationAPI = global.navigator.geolocation;
    const getUserLocation = () => {
      if (!geolocationAPI) {
        console.log("FUCK");
      } else {
        geolocationAPI.getCurrentPosition((position) => {
          const { coords } = position;
          setLat(coords.latitude);
          setLong(coords.longitude);
        });
      }
    };
    getUserLocation();
  }, []);

  console.log(weatherData);

  return (
    <div className={styles.container}>
      <Head>
        <title>The Weather App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <header className={styles.header}>
        <p>Boo!</p>
        <p>
          My coordinates are: {lat} / {long}
        </p>
      </header>
      <main className={styles.main}>
        <Weather />
      </main>

      <footer className={styles.footer}></footer>
    </div>
  );
};

export default Home;

export async function getServerSideProps() {
  let key: any = process.env.WEATHER_API_KEY;

  const res = await fetch(
    `http://dataservice.accuweather.com/locations/v1/cities/geoposition/search?apikey=${key}&q=${lat}%2C${long}`
  );
  const weatherData = await res.json();

  return {
    props: { weatherData },
  };
}
